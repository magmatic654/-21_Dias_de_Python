def print_triangle(size, character):
    blank_spaces = size - 1
    pieces = 1
    triangle = ''
    for i in range(1, size + 1):
        if i != 1 and i < size:
            triangle = triangle + "\n" + " " * blank_spaces + character * pieces
        elif i == 1:
            triangle = " "*blank_spaces + character *pieces
        else:
            triangle = triangle + "\n" + character *pieces
        pieces += 2
        blank_spaces -= 1
    print(triangle)
    return triangle

